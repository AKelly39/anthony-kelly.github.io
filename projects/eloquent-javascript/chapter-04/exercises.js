function reverseArrayInPlace(arr) {

  for (var i = 0; i < Math.floor(arr.length / 2); i++){
    var temp = arr[i];
    arr[i] = array[array.length - 1 - i];
    arr[arr.length - 1 - i] = temp;

  }
  
  return arr;

}

